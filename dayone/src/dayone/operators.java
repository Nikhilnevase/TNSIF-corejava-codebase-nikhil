package dayone;

public class operators {
	 public static void main (String[]args) {
		 // operators are  mathematical symboles  that use between two operands to perform specific task
		 // 1 increament operator 
		 int a = 1;
		 a=a++;    //post increament 
		 System.out.println(a);
		 ++a;      // pre increament 
		 System.out.println(a);
		 
		 // decreament operator 
		 
		 --a;   //pre decreament 
		 System.out.println(a);
		 a--;
		 System.out.println(a);
	 }

}
