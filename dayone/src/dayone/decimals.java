package dayone;

public class decimals {
	public static void main(String [] args) {
		//float is data type use store the decimal number with less roundoff 
		//example
		float a = 10f/3f;
		System.out.println(a);
		// double is the data type use to store the decimals with large roundoff 
		double b = 10d/3d;
		System.out.println(b);
		
	}

}
