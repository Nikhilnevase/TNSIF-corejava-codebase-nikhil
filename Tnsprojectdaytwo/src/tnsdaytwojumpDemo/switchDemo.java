package tnsdaytwojumpDemo;

public class switchDemo {
	public static void main (String[]args) {
		int userinput = 1;
		switch(userinput) {
		case 1 : System.out.print("hello my friend");
		break;
		
		case 2 : System.out.print("hello sir");
		break;
		
		case 3 : System.out.print("hello world");
		break;
		}
	}

}
